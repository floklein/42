int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int c;
	int n1;
	int n2;

	c = 0;
	while ((s1[c] != '\0' || s2[c] != '\0') && c != n)
	{
		if (s1[c] != s2[c])
		{
			n1 = s1[c];
			n2 = s2[c];
			if (n1 > n2 || n1 < n2)
				return (n1 - n2);
		}
		if (s1[c] == s2[c])
			c++;
	}
	return (0);
}
