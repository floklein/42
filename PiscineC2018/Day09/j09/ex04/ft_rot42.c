char	*ft_rot42(char *str)
{
	int		i;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 'a' && str[i] <= 'z')
		{
			str[i] -= 'a';
			str[i] = (str[i] + 10) % 26;
			str[i] += 'a';
		}
		if (str[i] >= 'A' && str[i] <= 'Z')
		{
			str[i] -= 'A';
			str[i] = (str[i] + 10) % 26;
			str[i] += 'A';
		}
		i++;
	}
	return (str);
}
