int		ft_sqrt(int nb)
{
	int		sqrt;

	sqrt = 0;
	if (nb != 2147483647)
		while (sqrt * sqrt != nb && sqrt * sqrt <= nb)
			sqrt++;
	return (sqrt * sqrt == nb ? sqrt : 0);
}
