#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char	*t;
	int		i;

	i = 0;
	t = ft_strnew(ft_strlen(s));
	while (s[i] != 0)
	{
		t[i] = f(s[i]);
		i++;
	}
	return (t);
}
