char	*ft_strcpy(char *dest, char *src)
{
	int index;

	index = 0;
	while (src[index] != '\0')
	{
		dest[index] = src[index];
		index++;
	}
	if (src[index] == '\0')
		dest[index] = src[index];
	return (dest);
}
