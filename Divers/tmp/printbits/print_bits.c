void	print_bits(unsigned char c)
{
	int		div;

	div = 128;

	while (div > 0)
	{
		if (div <= c)
		{
			write(1, "1", 1);
			c = c % div;
		}
		else
			write(1, "0", 1);
		div = div / 2;
	}
}

int		main(void)
{
	print_bits(2);
}
