char	*ft_strupcase(char *str)
{
	int c;
	int nb;

	c = 0;
	while (str[c] != '\0')
	{
		nb = str[c];
		if (nb >= 97 && nb <= 122)
		{
			str[c] = str[c] - 32;
		}
		c++;
	}
	return (str);
}
