char	*ft_strlowcase(char *str)
{
	int c;
	int nb;

	c = 0;
	while (str[c] != '\0')
	{
		nb = str[c];
		if (nb >= 65 && nb <= 90)
		{
			str[c] = str[c] + 32;
		}
		c++;
	}
	return (str);
}
