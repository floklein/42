#include "libft.h"

void	ft_putnbr(int n)
{
	if (n < 0)
	{
		if (n <= -10)
		{
			ft_putchar('-');
			ft_putnbr(-1 * (n / 10));
			ft_putchar('0' + -1 * (n % 10));
		}
		if (n > -10)
		{
			ft_putchar('-');
			ft_putchar('0' - n);
		}
	}
	else
	{
		if (n >= 10)
		{
			ft_putnbr(n / 10);
			ft_putchar('0' + n % 10);
		}
		else
			ft_putchar('0' + n);
	}
}
