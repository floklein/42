#include <stdio.h>

char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int	i;

	i = 0;
	while (i < n && src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	while (i < n)
	{
		dest[i] = '\0';
		i++;
	}
	return (dest);
}

int		main()
{
	char tabc[] = "abc";
	char tabc2[] = "dad";

	printf("%s", ft_strncpy(tabc, tabc2, 3));
}
