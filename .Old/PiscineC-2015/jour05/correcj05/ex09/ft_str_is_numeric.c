int	ft_str_is_numeric(char *str)
{
	int i;

	i = 0;
	if (str[i] == '\0')
		return (1);
	while ('0' <= str[i] && str[i] <= '9')
	{
		i++;
		if (str[i] == '\0')
			return (1);
	}
	return (0);
}
