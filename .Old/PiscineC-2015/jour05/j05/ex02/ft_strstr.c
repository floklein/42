char	*ft_strstr(char *str, char *tofind)
{
	int		i;
	int		j;

	i = 0;
	while (str[i] != '\0')
	{
		j = 0;
		while (tofind[j] == str[i + j])
		{
			if (tofind[j + 1] == '\0')
				return (str + i);
			else
				j++;
		}
		i++;
	}
	return (0);
}
